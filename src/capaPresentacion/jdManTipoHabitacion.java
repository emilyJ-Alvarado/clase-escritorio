     private void listarTipoHabitaciones(){
        ResultSet rsListaTH= null;
        String vig="";
        DefaultTableModel modelo= new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");
        modelo.addColumn("Descripción");
        modelo.addColumn("Vigencia");
        modelo.addColumn("Costo");
        tblTipoHabitacion.setModel(modelo);
        try {
            rsListaTH=objTH.listarTH();
            while(rsListaTH.next()){
               if(rsListaTH.getString("vigencia").equals("t")){
                   vig="Vigente";
               } else{
                   vig="No Vigente";
               }
               modelo.addRow(new Object[]{rsListaTH.getInt("codTipoHabitacion"),rsListaTH.getString("nombre"),rsListaTH.getString("descripcion"),vig,rsListaTH.getDouble("costo")});
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
