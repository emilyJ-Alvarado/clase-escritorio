    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
         ResultSet rsTH = null;
        try {
            if(txtCodigo.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Ingrese código a buscar...");
                txtCodigo.requestFocus();
            }else{
                rsTH=objTH.buscarTH( Integer.parseInt(txtCodigo.getText()));
                if(rsTH.next()){
                    txtNombre.setText(rsTH.getString("nombre"));
                    txtDescripcion.setText(rsTH.getString("descripcion"));
                    txtCosto.setText(String.valueOf(rsTH.getDouble("costo")));
                    chkVigencia.setSelected(rsTH.getBoolean("vigencia"));
                }else{
                    JOptionPane.showMessageDialog(this, "Código no registrado...");
                    limpiarControles();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

