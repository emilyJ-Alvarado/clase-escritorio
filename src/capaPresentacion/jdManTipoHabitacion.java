    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
      try {
            if (btnNuevo.getText().equals("Nuevo")){
                btnNuevo.setText("Guardar");
                limpiarControles();
                txtCodigo.setText(objTH.generarCodigoTH().toString());
                txtNombre.requestFocus();
            }else{
                btnNuevo.setText("Nuevo");
                //Guardar tipo habitacion
                objTH.registrarTH(Integer.parseInt(txtCodigo.getText()),txtNombre.getText(), txtDescripcion.getText(), chkVigencia.isSelected(),Double.parseDouble(txtCosto.getText()));
                limpiarControles();
                listarTipoHabitaciones();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }


    }//GEN-LAST:event_btnNuevoActionPerformed

