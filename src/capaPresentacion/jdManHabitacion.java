    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
     String est="";
        Integer codTH;
        try {
            if(btnNuevo.getText().equals("Nuevo")){
                btnNuevo.setText("Guardar");
                limpiarControles();
                txtCodigo.setText(objHab.generarCodigoHAB().toString());
            
            }else{
                btnNuevo.setText("Nuevo");
                if (objHab.verificarNumHabitacion(Integer.parseInt(txtNumero .getText()))){
                    if (cboEstado.getSelectedItem().equals("Disponible")) est="D";
                    if (cboEstado.getSelectedItem().equals("Mantenimiento")) est="M";
                    if (cboEstado.getSelectedItem().equals("Ocupada")) est="O";
                    codTH= objTH.obtenerCodigoTH( cboTipoHabitacion.getSelectedItem().toString());
                    objHab.registrarHab(Integer.parseInt(txtCodigo.getText()),Integer.parseInt(txtNumero.getText()), txtNombre.getText(),txtDescripcion.getText(), est, Integer.parseInt(spnCapacidad.getValue().toString()), codTH);
                    limpiarControles();
                    listarHabitaciones();
                }else{
                     JOptionPane.showMessageDialog(this, "Número de habitación está registrado!");
                     limpiarControles();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

