    private void btnDarBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarBajaActionPerformed
        // TODO add your handling code here:
        try {
            if(txtCodigo.getText().equals("")){
                  JOptionPane.showMessageDialog(this, "Ingrese el código de habitación a dar de baja!");
            }else{
                if (objHab.verificarDisponibilidad(Integer.parseInt(txtCodigo.getText()))){
                    objHab.darBajaHab(Integer.parseInt(txtCodigo.getText()));
                    limpiarControles();
                    listarHabitaciones();
                }else{
                    JOptionPane.showMessageDialog(this, "Habitación no está disponible!");
                }
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnDarBajaActionPerformed

