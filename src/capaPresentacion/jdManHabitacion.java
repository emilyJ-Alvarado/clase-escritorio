    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ResultSet rsHab=null;
       String est="";
        try {
            if(txtNumero.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Ingrese el número de habitación a buscar!");
            }else{
                rsHab=objHab.buscarHab(Integer.parseInt(txtNumero.getText()));
                if (rsHab.next()){
                    txtCodigo.setText(rsHab.getString("codHabitacion"));
                    txtNombre.setText(rsHab.getString("nombre"));
                    txtDescripcion.setText(rsHab.getString("descripcion"));
                    est=rsHab.getString("estado");
                    if(est.equals("D")) cboEstado.setSelectedItem("Disponible");
                    if(est.equals("O")) cboEstado.setSelectedItem("Ocupada");
                    if(est.equals("M")) cboEstado.setSelectedItem("Mantenimiento");
                    spnCapacidad.setValue(rsHab.getInt("capacidad"));
                    cboTipoHabitacion.setSelectedItem(rsHab.getString("nombreTH"));
                    
                }else{
                    JOptionPane.showMessageDialog(this, "Número de hbaitación no existe!");
                    limpiarControles();
                }
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

