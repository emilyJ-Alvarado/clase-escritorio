    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            if(txtDoc.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Ingrese documento a consultar");
            }else{
                rsHues=objHues.buscarHuesped(txtDoc.getText());
                if(rsHues.next()){
                    txtNom.setText(rsHues.getString("nombres"));
                    txtApellidos.setText(rsHues.getString("apellidos"));
                    codHuesped=rsHues.getInt("codHuesped");
                }else{
                   JOptionPane.showMessageDialog(this, "Documento no existe");
                   limpiarHuesped();
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnBuscarActionPerformed
 private void limpiarHuesped(){
        txtDoc.setText("");
        txtNom.setText("");
        txtApellidos.setText("");
        txtDoc.requestFocus();
    }
