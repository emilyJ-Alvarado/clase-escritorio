  private void listarHabitaciones(){
        ResultSet rsHab =null;
        DefaultTableModel modeloHab=new DefaultTableModel();
        modeloHab.addColumn("C√≥digo");
        modeloHab.addColumn("N√∫mero");
        modeloHab.addColumn("Descripci√≥n");
        modeloHab.addColumn("Capacidad");
        tblHabitacion.setModel(modeloHab);
        try {
            rsHab=objHab.listarHabitacionDisp(cboTipoHab.getSelectedItem().toString());
            while(rsHab.next()){
                modeloHab.addRow(new Object[]{rsHab.getInt("codhabitacion"),rsHab.getInt("numero"),rsHab.getString("descripcion"),rsHab.getInt("capacidad")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
