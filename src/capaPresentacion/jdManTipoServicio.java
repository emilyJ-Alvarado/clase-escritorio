    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
         ResultSet rsTS = null;
        try {
            if(txtCodigo.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Ingrese código a buscar...");
                txtCodigo.requestFocus();
            }else{
                rsTS=objTS.buscarTS( Integer.parseInt(txtCodigo.getText()));
                if(rsTS.next()){
                    txtNombre.setText(rsTS.getString("nombre"));
                    txtCostoBase.setText(String.valueOf(rsTS.getDouble("costobase")));
                    chkVigencia.setSelected(rsTS.getBoolean("vigencia"));
                }else{
                    JOptionPane.showMessageDialog(this, "Código no registrado...");
                    limpiarControles();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
         try {
            if (btnNuevo.getText().equals("Nuevo")){
                btnNuevo.setText("Guardar");
                limpiarControles();
                txtCodigo.setText(objTS.generarCodigoTH().toString());
                txtNombre.requestFocus();
            }else{
                btnNuevo.setText("Nuevo");
                //Guardar tipo habitacion
                objTS.registrarTS(Integer.parseInt(txtCodigo.getText()),txtNombre.getText(),Double.parseDouble(txtCostoBase.getText()), chkVigencia.isSelected());
                limpiarControles();
                listarTipoServicios();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

