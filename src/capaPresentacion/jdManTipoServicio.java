         private void listarTipoServicios(){
        ResultSet rsListaTS= null;
        String vig="";
        DefaultTableModel modelo= new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");
        modelo.addColumn("Costo Base");
        modelo.addColumn("Vigencia");
        tblTipoServicio.setModel(modelo);
        try {
            rsListaTS=objTS.listarTS();
            while(rsListaTS.next()){
               if(rsListaTS.getString("vigencia").equals("t")){
                   vig="Vigente";
               } else{
                   vig="No Vigente";
               }
               modelo.addRow(new Object[]{rsListaTS.getInt("codtiposer"),rsListaTS.getString("nombre"),rsListaTS.getDouble("costobase"),vig});
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void limpiarControles(){
        txtCodigo.setText("");
        txtNombre.setText("");
        txtCostoBase.setText("");
        chkVigencia.setSelected(false);
    }
